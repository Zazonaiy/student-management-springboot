package com.management.model;

public class Clazz {
	Integer id;
	String classNo;
	String className;
	Integer enterYear;
	String managerUserFk;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getClassNo() {
		return classNo;
	}
	public void setClassNo(String classNo) {
		this.classNo = classNo;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public Integer getEnterYear() {
		return enterYear;
	}
	public void setEnterYear(Integer enterYear) {
		this.enterYear = enterYear;
	}
	public String getManagerUserFk() {
		return managerUserFk;
	}
	public void setManagerUserFk(String managerUserFk) {
		this.managerUserFk = managerUserFk;
	}
	
	
}
